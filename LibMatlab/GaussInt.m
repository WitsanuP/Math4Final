function I = GaussInt(f,a,b,n)
% Code 5-5 : Gauss-Legendre
% f is function
% a and b boundary
% n is point to ref
%Method____________________________________________________-
switch n
    case 2
        X = [-0.577350269,  0.577350269];
        S = [1,             1];
    case 3
        X = [-0.774596669,  0 ,             0.774596669];
        S = [0.555555556,   0.888888889,   0.555555556];
    
    case 4
        X = [-0.861136312,  -0.339981044,   0.339981044,    0.861136312];
        S = [0.347854845,   0.652145155,    0.652145155,    0.347854845];
    case 5
        X = [-0.906179846,  -0.538469310,   0,              0.538469310,    0.906179846];
        S = [0.236926885,   0.478628670,    0.568888889,    0.478628670,    0.236926885];
    case 6
        X = [-0.932469514,  -0.661209386,   -0.238619186,   0.238619186,    0.661209386,    0.932469514];
        S = [0.171324492,   0.360761573,     0.467913935,   0.467913935,    0.360761573,    0.171324492];

end
    a0 = (b+a)/2;
    a1 = (b-a)/2;
    x = a1*X+a0;
    F = a1*f(x);
    I = S *F(:);
end